    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     String idText = jTextField4.getText();
    if(idText.isEmpty()){
        JOptionPane.showMessageDialog(this, "ID es obligatorio", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    try {
        int id = Integer.parseInt(idText);
        ResultSet consulta = crud.obtenerArticuloPorID(id);
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        
        if(consulta.next()) {
            modelo.addRow(new Object[]{
                consulta.getInt("id_articulo"),
                consulta.getString("nombre"), 
                consulta.getString("categoria"), 
                consulta.getInt("stock")
            });
        } else {
            JOptionPane.showMessageDialog(this, "No se encontr√≥ el art√≠culo", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch(SQLException b) {
        System.out.println("Error al llenar la tabla: " + b.getMessage());
        JOptionPane.showMessageDialog(this, "Error al buscar art√≠culo", "Error", JOptionPane.ERROR_MESSAGE);
    } catch(NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID debe ser num√©rico", "Error", JOptionPane.ERROR_MESSAGE);
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

